Day2

Let's reframe fear.
When was the last time you stopped yourself from doing something bacause you were afraid?
Let's be honest.
We all have the fear of not being good enough, so sometimes we don't even bother to try.
But what if you just choose to believe you are good enough?
And What if fear means you are heading towards the right direction?
The sensation you feel in your body is probadly telling you that you are stepping up your game.
The reason why you are scared is because you were brave enough to put yourself out there.
AS long as you are doing what's best for you, fear will always be there.
So instead, let's say. "Come on fear. You are coming along."

Day3

Do what scared you.
Do you want to know how to overcome your fears? Just do it scared.
What is the thing that you are most afraid to say or do?
I challenge you to do it today.
This is how you live your life to the fullest.
If you have something that feels true to your heart, iet it out.
Being scared should not be the sign to stop.
In fact, the bigger your goal is, the more you are supposed to feel scared.
If you feel totally comfortable with your dreams, set higher golas.
Speak your truth. Do what you're meant to do.
Be willing to feel scared.
Then, you will not be scared of that anymore.
You'll find another thing that makes you scared.
The more you repeat this process, the more you can create an amazing life.

